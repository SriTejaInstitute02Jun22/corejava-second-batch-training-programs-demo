package com.sriteja.operators;

/**
 * 	This class is having Ternary Operators
 *  Syntax:- condition ?  : ;
 * **/
public class TernaryOperators {

	public static void main(String[] args) {
		
		int i = 15;
		int j = 10;
		int k = 15;
		
		System.out.println((i<=j)?"I am happy":"I am not happy");
		
		System.out.println((i>=j)?"I am happy":"I am not happy");
		
		System.out.println((i==k)?"I am happy":"I am not happy");

	}

}
