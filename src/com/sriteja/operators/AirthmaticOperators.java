package com.sriteja.operators;

/**
 * this class is using to create the Arithmetic Operators
 * symbols: +, -, *, /,	%
 * */
public class AirthmaticOperators {

	public static void main(String args[]) {
		
		//crating the instance variables
		int i = 10;
		int j = 5;
		
		System.out.println("I+J = "+(i+j)); //i+j = 10+5;
		System.out.println("I-J = "+(i-j)); //i-j = 10-5;
		System.out.println("I*J = "+(i*j));
		System.out.println("I/J = "+(i/j));
		System.out.println("I%J = "+(i%j));
	}
}
