package com.sriteja.operators;

/**
 * this class is going to create the Relational Operators
 * symbols: <, >, <=, >=, instanceof, ==, !=
 * */
public class RelationalOperators {

	public static void main(String[] args) {

		int i = 8;
		int k = 4;
		
		System.out.println("I<K = "+(i<k));	
		System.out.println("I>K = "+(i>k));
		System.out.println("I<=K = "+(i<=k));
		System.out.println("I>=K = "+(i>=k));
		System.out.println("I==K = "+(i==k));
		System.out.println("I!=K = "+(i!=k));
		
		
	}

}
